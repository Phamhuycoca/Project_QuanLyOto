      public void loadtable(){
         try{
        String get="SELECT * FROM NhanVien";
        //String get="SELECT * FROM NhanVien";
        pst=conn.prepareStatement(get);
        rs=pst.executeQuery();
        ResultSetMetaData stData=rs.getMetaData();
        int kq=stData.getColumnCount();
        DefaultTableModel RecordTable= (DefaultTableModel)Table.getModel();
        RecordTable.setRowCount(0);
        while(rs.next()){
            Vector columnData=new Vector();
            for(int i=0;i<=kq;i++){
                columnData.add(rs.getString(1));
                columnData.add(rs.getString(2));
                if(rs.getInt(3)==0){
                columnData.add("Nam");
                }else{
                columnData.add("Nữ");

                }
                columnData.add(rs.getString(4));
                columnData.add(rs.getString(5));
                 if(rs.getInt(6)==0){
                columnData.add("Quản lý");
                }else{
                columnData.add("Nhân viên");
                //id=rs.getString(9);
                }
            }
            RecordTable.addRow(columnData);
         }
         }catch(Exception ex){
             System.out.println(ex.toString());
         }
    }
