    private void TableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TableMouseClicked
        // TODO add your handling code here:
        //có chức năng để lấy mô hình dữ liệu (model) 
        //của một đối tượng JTable và gán cho một biến kiểu DefaultTableModel.
         model=(DefaultTableModel)Table.getModel();
        int c=Table.getSelectedRow();
        txtMaNhanVien.setText(model.getValueAt(c, 0).toString());
        txtTenNhanVien.setText(model.getValueAt(c, 1).toString());
        String sex=model.getValueAt(c, 2).toString();
        if(sex.equals("Nam")){
            radioNam.setSelected(true);
        }
        if(sex.equals("Nữ")){
            radioNu.setSelected(true);
        }        
      
        txtDiaChi.setText(model.getValueAt(c, 3).toString());
        txtSdt.setText(model.getValueAt(c, 4).toString());
        String cv=model.getValueAt(c, 5).toString();
        if(cv.equals("Quản lý")){
            CbChucVu.setSelectedItem("Quản lý");
        }else{
            CbChucVu.setSelectedItem("Nhân viên");
        }
         int i = Integer.parseInt(txtMaNhanVien.getText());
        Use(i);
    }//GEN-LAST:event_TableMouseClicked

